// Questão 10

#include <stdio.h>

int main() {
    int num1, num2, soma;

    // Leitura dos dois números inteiros
    printf("Digite o primeiro número: ");
    scanf("%d", &num1);
    printf("Digite o segundo número: ");
    scanf("%d", &num2);

    // Cálculo da soma dos dois números
    soma = num1 + num2;

    // Exibição do resultado da soma
    printf("A soma é: %d\n", soma);

    return 0;
}
