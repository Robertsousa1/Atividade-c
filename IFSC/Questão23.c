#include <stdio.h>

int main() {
    int numA, numB;

    // Leitura dos valores NumA e NumB
    printf("Digite o valor de A: ");
    scanf("%d", &numA);
    printf("Digite o valor de B: ");
    scanf("%d", &numB);

    // Exibição dos valores em ordem inversa
    printf("Os valores em ordem inversa são: %d e %d\n", numB, numA);

    return 0;
}
